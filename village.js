let currentLang = 'mr';

function applyLanguage(lang) {
  currentLang = lang;
  const t = translations[lang];

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key]) {
      if (key === 'about_p1') {
        el.innerHTML = t[key];
      } else {
        el.textContent = t[key];
      }
    }
  });

  document.querySelectorAll('[data-i18n-num]').forEach(el => {
    const key = el.getAttribute('data-i18n-num');
    if (t[key]) el.textContent = t[key];
  });

  document.documentElement.lang = lang;

  document.title = lang === 'mr'
    ? 'मस्करवाडी | शांतता आणि प्रगतीचे गाव'
    : 'Maskarwadi | A Village of Peace & Progress';

  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc) {
    metaDesc.content = lang === 'mr'
      ? 'मस्करवाडी — महाराष्ट्राच्या हृदयातील एक शांत गाव, संस्कृती, समुदाय आणि नैसर्गिक सौंदर्याने समृद्ध.'
      : 'Maskarwadi — A peaceful village in the heart of Maharashtra, rich in culture, community, and natural beauty.';
  }

  document.querySelectorAll('.lang-btn').forEach(btn => {
    const isActive = btn.getAttribute('data-lang') === lang;
    btn.classList.toggle('active', isActive);
    btn.setAttribute('aria-pressed', isActive);
  });

  try { localStorage.setItem('maskarwadi_lang', lang); } catch(e) {}
}

function setLanguage(lang) {
  if (lang === currentLang) return;
  applyLanguage(lang);
}

